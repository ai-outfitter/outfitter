import { cp, mkdir, mkdtemp, readFile, rm, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { homedir, tmpdir } from 'node:os';
import { dirname, isAbsolute, join } from 'node:path';
import { parse as parseYaml, stringify } from 'yaml';
import { runAgent } from './adapters.ts';
import { runJudge } from './evaluate.ts';
import { gradeOutputs, gradeQuestions, measureCoverage, runChecks } from './grade.ts';
import type {
  AssertionResult,
  EvalDefinition,
  EvalSummary,
  EvaluationResult,
  MatrixVariant,
  RunMetrics,
  RunResult,
  VariantSummary,
} from './types.ts';

export interface RunEvalOptions {
  readonly resultsRoot: string;
  readonly variantFilter?: string;
  readonly repeatOverride?: number;
  readonly keepWorkspaces?: boolean;
  readonly log?: (message: string) => void;
}

const DEFAULT_VARIANT: MatrixVariant = { id: 'default' };

const resolveVariants = (definition: EvalDefinition, filter?: string): readonly MatrixVariant[] => {
  const variants = definition.matrix?.variants ?? [DEFAULT_VARIANT];
  if (filter === undefined) {
    return variants;
  }
  const filtered = variants.filter((variant) => variant.id === filter);
  if (filtered.length === 0) {
    throw new Error(
      `eval ${definition.id}: unknown variant "${filter}" (have: ${variants.map((v) => v.id).join(', ')})`,
    );
  }
  return filtered;
};

const isRecord = (value: unknown): value is Record<string, unknown> =>
  typeof value === 'object' && value !== null && !Array.isArray(value);

export const deepMerge = (
  base: Record<string, unknown>,
  overrides: Record<string, unknown>,
): Record<string, unknown> => {
  const merged: Record<string, unknown> = { ...base };
  for (const [key, value] of Object.entries(overrides)) {
    const existing = merged[key];
    merged[key] = isRecord(existing) && isRecord(value) ? deepMerge(existing, value) : value;
  }
  return merged;
};

/**
 * Materialize a variant as a project-local profile in the workspace that
 * inherits from the base profile, so the launcher resolves it like any other
 * profile. Outfitter only reads project profiles declared via profile_sources,
 * so a workspace .outfitter/settings.yml exposing ./profiles is written too.
 */
const materializeVariantProfile = async (
  definition: EvalDefinition,
  variant: MatrixVariant,
  workspace: string,
): Promise<string> => {
  const baseProfile = definition.matrix?.base_profile ?? definition.profile;
  if (variant.overrides === undefined || Object.keys(variant.overrides).length === 0) {
    return baseProfile;
  }
  const profileId = `${definition.id}--${variant.id}`;
  const profileDocument = deepMerge(
    {
      id: profileId,
      label: `${definition.id} / ${variant.id}`,
      description: `Generated by agentic-evals for eval ${definition.id}, variant ${variant.id}.`,
      inherits: [baseProfile],
    },
    { ...variant.overrides },
  );
  const profileDirectory = join(workspace, '.outfitter', 'profiles', profileId);
  await mkdir(profileDirectory, { recursive: true });
  await writeFile(join(profileDirectory, 'profile.yml'), stringify(profileDocument), 'utf8');
  const settingsPath = join(workspace, '.outfitter', 'settings.yml');
  if (!existsSync(settingsPath)) {
    const sources = [{ path: './profiles' }, ...(await readUserProfileSources())];
    await writeFile(settingsPath, stringify({ profile_sources: sources }), 'utf8');
  }
  return profileId;
};

/**
 * Project profile_sources shadow the user's, so the workspace settings must
 * re-declare whatever sources provide the base profile the variant inherits
 * from. Relative local paths are absolutized against ~/.outfitter; remote
 * sources (github etc.) keep their repo-relative path untouched.
 */
const readUserProfileSources = async (): Promise<Record<string, unknown>[]> => {
  const userSettingsPath = join(homedir(), '.outfitter', 'settings.yml');
  if (!existsSync(userSettingsPath)) {
    return [];
  }
  const document = parseYaml(await readFile(userSettingsPath, 'utf8')) as {
    profile_sources?: Record<string, unknown>[];
  } | null;
  return (document?.profile_sources ?? []).map((source) => {
    const path = source['path'];
    const isLocalOnly = Object.keys(source).every((key) => ['path', 'only', 'except'].includes(key));
    if (isLocalOnly && typeof path === 'string' && !isAbsolute(path)) {
      return { ...source, path: join(homedir(), '.outfitter', path) };
    }
    return source;
  });
};

const prepareWorkspace = async (definition: EvalDefinition, label: string): Promise<string> => {
  const workspace = await mkdtemp(join(tmpdir(), `agentic-eval-${definition.id}-${label}-`));
  const fixtures = join(definition.directory, 'fixtures');
  if (existsSync(fixtures)) {
    await cp(fixtures, workspace, { recursive: true });
  }
  for (const input of definition.inputs) {
    await cp(join(definition.directory, input.from), join(workspace, input.to), { recursive: true });
  }
  return workspace;
};

const executeRun = async (
  definition: EvalDefinition,
  variant: MatrixVariant,
  runNumber: number,
  options: RunEvalOptions,
  artifactCaptureDirectory?: string,
  transcriptPath?: string,
): Promise<RunResult> => {
  const workspace = await prepareWorkspace(definition, `${variant.id}-${String(runNumber)}`);
  try {
    const profileId = await materializeVariantProfile(definition, variant, workspace);
    const agentResult = await runAgent({ definition, profileId, variantId: variant.id, workspace });
    if (transcriptPath !== undefined) {
      await mkdir(dirname(transcriptPath), { recursive: true });
      await writeFile(
        transcriptPath,
        `--- stdout ---\n${agentResult.stdout}\n--- stderr ---\n${agentResult.stderr}\n`,
        'utf8',
      );
    }

    const assertions: AssertionResult[] = [
      ...(await gradeOutputs(definition, workspace)),
      ...(await runChecks(definition, workspace)),
      ...(await gradeQuestions(definition, workspace)),
    ];

    const metrics: RunMetrics = {
      wall_time_seconds: agentResult.metrics.wall_time_seconds ?? 0,
      tokens_in: agentResult.metrics.tokens_in ?? null,
      tokens_out: agentResult.metrics.tokens_out ?? null,
      tool_calls: agentResult.metrics.tool_calls ?? null,
      turns: agentResult.metrics.turns ?? null,
      cost_usd: agentResult.metrics.cost_usd ?? null,
      coverage_percent: null,
    };

    if (definition.metrics?.coverage !== undefined) {
      const coverage = await measureCoverage(definition.metrics.coverage, workspace);
      metrics.coverage_percent = coverage.percent;
      if (coverage.assertion !== undefined) {
        assertions.push(coverage.assertion);
      }
    }

    if (agentResult.timedOut) {
      assertions.push({ name: 'agent completed within timeout', passed: false, detail: 'run timed out' });
    }

    if (artifactCaptureDirectory !== undefined && definition.evaluation !== undefined) {
      for (const input of definition.evaluation.inputs) {
        const artifact = join(workspace, input.artifact);
        if (existsSync(artifact)) {
          await mkdir(dirname(join(artifactCaptureDirectory, input.artifact)), { recursive: true });
          await cp(artifact, join(artifactCaptureDirectory, input.artifact), { recursive: true });
        }
      }
    }

    const passedCount = assertions.filter((assertion) => assertion.passed).length;
    return {
      eval: definition.id,
      variant: variant.id,
      run: runNumber,
      agent: definition.agent,
      profile: profileId,
      passed: passedCount === assertions.length && !agentResult.timedOut,
      timed_out: agentResult.timedOut,
      exit_code: agentResult.exitCode,
      assertions: { passed: passedCount, total: assertions.length, results: assertions },
      metrics,
      workspace: options.keepWorkspaces === true ? workspace : undefined,
    };
  } finally {
    if (options.keepWorkspaces !== true) {
      await rm(workspace, { recursive: true, force: true });
    }
  }
};

const summarizeVariant = (variantId: string, runs: readonly RunResult[]): VariantSummary => {
  const passed = runs.filter((run) => run.passed).length;
  const meanWallTime = runs.reduce((total, run) => total + run.metrics.wall_time_seconds, 0) / runs.length;
  return {
    variant: variantId,
    runs: runs.length,
    passed,
    pass_rate: Number((passed / runs.length).toFixed(3)),
    mean_wall_time_seconds: Number(meanWallTime.toFixed(3)),
  };
};

export interface EvalRunOutcome {
  readonly summary: EvalSummary;
  readonly runs: readonly RunResult[];
  readonly resultsDirectory: string;
}

export const runEval = async (definition: EvalDefinition, options: RunEvalOptions): Promise<EvalRunOutcome> => {
  const log = options.log ?? (() => undefined);
  const startedAt = new Date().toISOString();
  const stamp = startedAt.replaceAll(':', '-').replace(/\.\d+Z$/, 'Z');
  const resultsDirectory = join(options.resultsRoot, definition.id, stamp);
  const variants = resolveVariants(definition, options.variantFilter);
  const repeat = options.repeatOverride ?? definition.matrix?.repeat ?? 1;

  const allRuns: RunResult[] = [];
  const variantSummaries: VariantSummary[] = [];

  for (const variant of variants) {
    const variantRuns: RunResult[] = [];
    for (let runNumber = 1; runNumber <= repeat; runNumber += 1) {
      log(`eval ${definition.id} variant ${variant.id} run ${String(runNumber)}/${String(repeat)}…`);
      // The judge grades run 1's artifacts, one directory per variant.
      const artifactCaptureDirectory =
        definition.evaluation !== undefined && runNumber === 1
          ? join(resultsDirectory, variant.id, 'artifacts')
          : undefined;
      const result = await executeRun(
        definition,
        variant,
        runNumber,
        options,
        artifactCaptureDirectory,
        join(resultsDirectory, variant.id, `run-${String(runNumber)}.transcript.log`),
      );
      log(
        `  ${result.passed ? 'PASS' : 'FAIL'} (${String(result.assertions.passed)}/${String(result.assertions.total)} assertions, ${String(result.metrics.wall_time_seconds)}s)`,
      );
      variantRuns.push(result);
      const runDirectory = join(resultsDirectory, variant.id);
      await mkdir(runDirectory, { recursive: true });
      await writeFile(
        join(runDirectory, `run-${String(runNumber)}.json`),
        `${JSON.stringify(result, null, 2)}\n`,
        'utf8',
      );
    }
    allRuns.push(...variantRuns);
    variantSummaries.push(summarizeVariant(variant.id, variantRuns));
  }

  let evaluation: EvaluationResult | undefined;
  if (definition.evaluation !== undefined) {
    const variantArtifacts = new Map<string, string>();
    for (const variant of variants) {
      const captured = join(resultsDirectory, variant.id, 'artifacts');
      if (existsSync(captured)) {
        variantArtifacts.set(variant.id, captured);
      } else {
        log(`  judge: variant ${variant.id} produced no artifacts to grade`);
      }
    }
    if (variantArtifacts.size > 0) {
      log(
        `judging ${String(variantArtifacts.size)} variant artifact set(s) with profile ${definition.evaluation.evaluator_profile}…`,
      );
      evaluation = await runJudge(definition, variantArtifacts);
      for (const score of evaluation.scores) {
        log(`  ${score.variant}: ${String(score.weighted_score)}/10 weighted`);
      }
    }
  }

  const summary: EvalSummary = {
    eval: definition.id,
    started_at: startedAt,
    agent: definition.agent,
    variants: variantSummaries,
    passed: allRuns.every((run) => run.passed),
    evaluation,
  };
  await mkdir(resultsDirectory, { recursive: true });
  await writeFile(join(resultsDirectory, 'summary.json'), `${JSON.stringify(summary, null, 2)}\n`, 'utf8');

  return { summary, runs: allRuns, resultsDirectory };
};
